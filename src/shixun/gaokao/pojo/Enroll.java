package shixun.gaokao.pojo;

public class Enroll {
	private String id;
	private String collegeCode;
	private String proCode;
	private String area;
	private int year;
	private String batch;
	private int maxEnrollScore;
	private int avgEnrollScore;
	private int minEnrollScore;
	private int enrollStuNum;
	private String acceptLiberalStu;
	private String acceptSciencesStu;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCollegeCode() {
		return collegeCode;
	}
	public void setCollegeCode(String collegeCode) {
		this.collegeCode = collegeCode;
	}
	public String getProCode() {
		return proCode;
	}
	public void setProCode(String proCode) {
		this.proCode = proCode;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public String getBatch() {
		return batch;
	}
	public void setBatch(String batch) {
		this.batch = batch;
	}
	public int getMaxEnrollScore() {
		return maxEnrollScore;
	}
	public void setMaxEnrollScore(int maxEnrollScore) {
		this.maxEnrollScore = maxEnrollScore;
	}
	public int getAvgEnrollScore() {
		return avgEnrollScore;
	}
	public void setAvgEnrollScore(int avgEnrollScore) {
		this.avgEnrollScore = avgEnrollScore;
	}
	public int getMinEnrollScore() {
		return minEnrollScore;
	}
	public void setMinEnrollScore(int minEnrollScore) {
		this.minEnrollScore = minEnrollScore;
	}
	public int getEnrollStuNum() {
		return enrollStuNum;
	}
	public void setEnrollStuNum(int enrollStuNum) {
		this.enrollStuNum = enrollStuNum;
	}
	public String getAcceptLiberalStu() {
		return acceptLiberalStu;
	}
	public void setAcceptLiberalStu(String acceptLiberalStu) {
		this.acceptLiberalStu = acceptLiberalStu;
	}
	public String getAcceptSciencesStu() {
		return acceptSciencesStu;
	}
	public void setAcceptSciencesStu(String acceptSciencesStu) {
		this.acceptSciencesStu = acceptSciencesStu;
	}	
	
}
